package org.knime.rest.nodes.common.webui;

import java.util.List;
import java.util.stream.Collectors;

import org.knime.core.node.InvalidSettingsException;
import org.knime.node.parameters.widget.choices.Label;

/**
 * The different types of authentication supported for REST calls.
 *
 * @author Magnus Gohm, KNIME GmbH, Konstanz, Germany
 */
enum RestAuthenticationType {

    @Label(value = "None", description = "No authentication is required.")
    NONE("None"), //
    @Label(value = "Basic auth", description = "Authentication with username and password.")
    BASIC_AUTH("Basic auth"), //
    @Label(value = "Bearer token", description = "Authentication with a bearer token.")
    BEARER_TOKEN("Bearer token"), //
    @Label(value = "Digest auth", //
        description = "Authentication with username and password using digest authentication.")
    DIGEST_AUTH("Digest auth"), //
    @Deprecated
    @Label(value = "NTLM", description = "Authentication with username and password using NTLM authentication.",
        disabled = true)
    NTLM_AUTH("NTLM"), //
    @Label(value = "Kerberos", description = "Kerberos ticket based authentication")
    KERBEROS("Kerberos");

    private final String m_value;

    RestAuthenticationType(final String value) {
        m_value = value;
    }

    static RestAuthenticationType getFromValue(final String value) throws InvalidSettingsException {
        for (final RestAuthenticationType authType : values()) {
            if (authType.m_value.equals(value)) {
                return authType;
            }
        }
        throw new InvalidSettingsException(createInvalidSettingsExceptionMessage(value));
    }

    private static String createInvalidSettingsExceptionMessage(final String name) {
        var values = List.of("None", "Basic auth", "Bearer token", "Digest auth", "NTLM", "Kerberos",
            "Authenticatin via Input port").stream().collect(Collectors.joining(", "));
        return String.format("Invalid value '%s'. Possible values: %s", name, values);
    }

}
